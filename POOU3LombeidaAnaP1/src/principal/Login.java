     public void validarUsuario(){
    int resultado =0;
        String log_password = String.valueOf(txtpass.getPassword());
        String log_usuario = txtusuario.getText();
          String SQL = "SELECT * FROM login where log_usuario='" + log_usuario + "'and log_password='"+ log_password + "'";
        
        try{
            Statement st=(Statement) con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            if(rs.next()){
                resultado=1;
               if(resultado==1){
                   Menu inicio = new Menu();
                   inicio.setVisible(true);
                   this.dispose();
                   JOptionPane.showMessageDialog(null,"Bienvenido");    
               } 
               }else{
                   JOptionPane.showMessageDialog(null,"Usuario Incorrecto");
            }                    
          }catch (Exception e){
              JOptionPane.showMessageDialog(null,"ERROR LOGIN"+e.getMessage());
          }
    
    }
